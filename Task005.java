public class Main {


    static void add() {
        int a = 2;
        int b = 3;
        int sum = a + b;
        System.out.println("Sum: " + sum);

    }
    static void subtract() {
        int a = 2;
        int b = 3;
        int Sub = a - b;
        System.out.println("Sub: " + Sub);
    }
    static void Mul() {
        int a = 2;
        int b = 3;
        int mul = a * b;
        System.out.println("Multiple: " + mul);
    }

    static void divide() {
        int a = 2;
        int b = 3;
        int div = a / b;
        System.out.println("divide: " + div);
    }

    public static void main(String[] args) {

        add();
        subtract();
        Mul();
        divide();

    }
}